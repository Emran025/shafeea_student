final List<QuranPage> quranPageList = _rawQuranPages
    .map((e) => QuranPage(
          page: e[2] as int,
          fromSurah: e[1] as String,
          fromAyah: e[3] as int,
          toSurah: e[4] as String,
          toAyah: e[6] as int,
        ))
    .toList();

/// Looks up the page info for a given Mushaf page number (1..604).
QuranPage quranPageInfo(int page) {
  final int clamped = page.clamp(1, kTotalQuranPages);
  return quranPageList[clamped - 1];
}

/// Which Juz' (1..30) a given page belongs to.
int juzForPage(int page) {
  for (final j in quranJuzList) {
    if (page >= j.fromPage && page <= j.toPage) return j.index;
  }
  return quranJuzList.last.index;
}

