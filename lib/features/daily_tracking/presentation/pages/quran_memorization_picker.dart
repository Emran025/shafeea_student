                              ayah: fromInfo.fromAyah,
                              juz: juzForPage(lowPage),
                            ),
                            if (toInfo != null) ...[
                              const SizedBox(height: 6),
                              _SummaryLine(
                                label: 'إلى',
                                surah: toInfo.toSurah,
                                page: highPage!,
                                ayah: toInfo.toAyah,
                                juz: juzForPage(highPage),
                              ),
                            ],
                            if (signedPages != null) ...[
                              const SizedBox(height: 6),
                              Row(
                                children: [
                                  const Text('عدد الصفحات: ',
