/// DIALOG — a continuous, page-accurate "scrubber" strip. The 30 Juz'
/// still show as visual chapter divisions, but the user can start/end
/// anywhere — including the middle of a Juz — at single-page precision.
/// Surah/ayah for both ends always comes from [quranPageList].
/// ---------------------------------------------------------------------
///
/// DIRECTION / SIGN RULE (unchanged from before, now page-accurate):
///   - anchor (first point placed) has a LOWER page number than the
///     second point ("from the right" — toward An-Nās) -> POSITIVE.
///   - anchor has a HIGHER page number ("from the left" — toward
///     Al-Fātiḥah) -> NEGATIVE.
/// The page count itself is just `abs(highPage - lowPage) + 1` (a plain
/// page number, no slash), signed per the rule above.
Future<void> showQuranMemorizationPickerDialog({
  required BuildContext context,
  int? initialFromPage,
  int? initialToPage,
  required void Function(
    int fromPage,
    int toPage,
    QuranPage fromInfo,
    QuranPage toInfo,
    int signedPages,
