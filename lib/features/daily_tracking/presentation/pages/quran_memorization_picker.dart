                          child: ElevatedButton(
                            onPressed: (fromInfo != null && toInfo != null && signedPages != null)
                                ? () {
                                    onConfirm(lowPage!, highPage!, fromInfo, toInfo, signedPages!);
                                    Navigator.of(ctx).pop();
                                  }
                                : null,
                            style: ElevatedButton.styleFrom(
                              backgroundColor: AppColors.accent,
                              shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(12)),
                            ),
                            child: const Text('تأكيد'),
                          ),
                        ),
                      ],
                    ),
                  ],
                ),
              ),
            ),
          ),
        );
      },
    ),
  );
}

/// A small draggable circular handle positioned on the strip. `x` is
/// measured from the right edge (to match the RTL page-1-on-the-right
/// layout used throughout this widget). `onDragToLeftDx` reports the new
/// finger position as a LEFT-measured dx (0 = left edge of the strip),
/// ready to feed straight into `pageForLocalDx`.
class _StripThumb extends StatelessWidget {
  final double x;
  final double totalWidth;
  final Color color;
  final ValueChanged<double> onDragToLeftDx;

  const _StripThumb({
    required this.x,
    required this.totalWidth,
    required this.color,
    required this.onDragToLeftDx,
  });

  @override
  Widget build(BuildContext context) {
    const double size = 22;
    return Positioned(
      right: (x - size / 2).clamp(-size / 2, totalWidth - size / 2),
      top: 0,
      bottom: 0,
      child: Builder(
        builder: (thumbContext) => GestureDetector(
          behavior: HitTestBehavior.opaque,
          onHorizontalDragUpdate: (details) {
            // Find the strip's own RenderBox (the closest ancestor Stack)
            // so we can turn a global drag position into a LEFT-measured
            // local dx regardless of the RTL `right:` positioning above.
            final RenderBox? stripBox =
                thumbContext.findAncestorRenderObjectOfType<RenderStack>();
            if (stripBox == null) return;
            final Offset local = stripBox.globalToLocal(details.globalPosition);
            onDragToLeftDx(local.dx);
          },
          child: Center(
            child: Container(
              width: size,
              height: size,
              decoration: BoxDecoration(
                shape: BoxShape.circle,
                color: color,
                border: Border.all(color: Colors.black26, width: 1.5),
                boxShadow: const [BoxShadow(color: Colors.black38, blurRadius: 4, offset: Offset(0, 1))],
              ),
            ),
          ),
        ),
      ),
    );
  }
}

class _SummaryLine extends StatelessWidget {
