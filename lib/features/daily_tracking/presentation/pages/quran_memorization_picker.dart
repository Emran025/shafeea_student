        final int? lowPage = (anchorPage != null && secondPage != null)
            ? (anchorPage! <= secondPage! ? anchorPage : secondPage)
            : null;
        final int? highPage = (anchorPage != null && secondPage != null)
            ? (anchorPage! <= secondPage! ? secondPage : anchorPage)
            : null;

